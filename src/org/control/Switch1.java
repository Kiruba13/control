package org.control;

public class Switch1 {
	public static void main(String[] args) {
		
		String s= "sat" ;
		switch (s) {
		case ("sat"):
			System.out.println("Saturday");
		break;
		
		case ("sun"):
			System.out.println("Sunday");
		break;
		
		default:
		System.out.println("Weekdays");
		
		}
		
		
	}

}
